        <h4>Pull ups</h4>
        <p>Pull ups kan je eigenlijk overal wel doen als je ergens aan kan hangen. Het is een simpele oefening. Om een pull up op de
            juiste manier te doen moet je aan een stang hangen, spanning op je rug en core zetten en dan moet je jezelf omhoog trekken.
            je hebt hier ook meerdere varianten op. Een hele bekende is een chin up. Dit is een pullup waarmee je je kin boven de stang moet 
            krijgen. Dit is een compound oefening. dit houd in dat je meerdere spieren traint. hiermee train je bijna je hele rug, je core en je 
            armen. Als je dit goed en snel doet krijg je het after-burn effect. Dit zorgt ervoor dat je calorieën blijft verbranden ook als je 
            al bent gestopt. Je verbrand ongeveer 10 calorieën per minuut.
        </p>
