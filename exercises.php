        <h4>Push Ups</h4>
        <p>Push up is een bekende oefening. Om deze oefening uit te voeren ga je in een plak positie zitten. je ze je handen op de grond 
           naast je lichaam. Je laat jezelf zakken en als je bijna de grond aanraakt ga je naar boven. Hier zijn meerdere varianten op.
           denk aan de diamond push up, waarbij je je handen in een diamanten vorm houd en dan een push up doet of een push up met klap, waarbij
           je jezelf van de grond weg drukt, in de lucht klapt en dan weer in de pushup stand staan. Met deze oefening train je je triceps, borsten
           en je schouders. Ook train je je biceps en je core hier een klein beetje mee. Per 20 pushups verbrand je ongeveer 10 calorieën, dus als je hier goed in word kan je redelijk wat verbranden per dag.
        </p>
