        <h4>Hardlopen</h4>
        <p>Hardlopen is een van de meest bekende oefeningen om snel gewicht te verliezen.
            Het is gezond omdat je lekker kan bewegen en er zijn veel variaties op. je kunt bijvoorbeeld interval
            training doen. Dit houd in dat je bijvoorbeeld 2 minuten rent en 1 minuut loopt. dit kan je opbouwen zodat
            je steeds langer kan rennen. Je kunt ook in een keer bijvoorbeeld 5 km gaan rennen als je al een goede conditie hebt.
            Ook kan je gewoon sneller lopen. Natuurlijk zal je op een hoger tempo sneller afvallen maar dit is een goede manier
            om te beginnen. Hoeveel calorieën je verbrand tijdens het hardlopen verschilt per persoon.
            Dit kun je berekenen met meerdere apps, maar de gemiddelde hardloper verbrand ongeveer 100 calorieën per gelopen km.
        </p>
