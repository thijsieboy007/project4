        <h4>Touwtje springen</h4>
        <p>Veel mensen zullen aan kinderen denken als ze denken aan touwtje spingen. Veel kinderen doen dat ook, maar kinderen zijn
            niet de enige. Veel sporters doen dit ook. Dit is omdat touwtje springen heel veel calorieën verbrand en tegelijkertijd
            ook bijna al je spieren traint. Touwtje springen is een full body workout. Het traint je armen, benen, gluten, rug, core en
            je borsten. Het is een van de beste oefeningen die je zelf thuis kan doen want het enige wat je nodig hebt is een stuk touw.
            Met touwtje springen verbrand je ongeveer 0,14 calorieën per kilo gewicht. Al zou je 80 kilo wegen, en je springt 10 minuten,
            verbrand je dus ongeveer 112 calorieën.
