        <h4>Sit ups</h4>
        <p>Een sit up is een hele makkelijke oefening. Hij traint ook alleen je core en is dus geen compound oefening, maar hij is wel 
            heel effectief. Je voert de oefening uit door te gaan liggen op de grond. Je doet je knieën omhoog maar houd je voeten op de 
            grond. Dit is de start positie. Nu ga je met je hoofd en een gedeelte van je bovenlichaam naar je knieën (omhoog dus). dit doe je
            ongeveer 3 reps van 15 keer. met deze oefening hoor je je buik te gaan voelen. er zijn ook meer varianten op. Je kunt bijvoorbeeld 
            ook met je benen in de lucht en dan zo hoog mogelijk komen met je bovenlichaam. je verbrand wel maar 16 calorieën per 100 sit ups dus 
            deze oefening is meer voor spier opbouw.
        </p>
