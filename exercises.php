        <h4>Leg raises</h4>
        <p> De leg rais is een oefening die je overal kan doen, maar weinig mensen echt doen. dit is jammer aangezien het een hele goede oefening is 
            Het is een oefening die de bovenbuik spieren, onderbuik spieren, obliques (zijkant buikspieren) en je rug. Je voert deze oefening uit door
            op de grond te liggen. Je houd je benen recht en tilt ze omhoog. Als ze bijna helemaal bovenaan zijn stop je want anders ga je leunen 
            op je rug en dat kan zorgen voor klachten. je gaat dan langzaam terug tot je bijna de grond aanraakt en dan ga je weer terug omhoog. Al
            zou je 80 kg wegen verbrand je zo een 129 kcal per 15 minuten achter elkaar. 
        </p>
